for i in range(1, 2):
    print(i)

# range(1, 2) → generira samo broj 1
# petlja se izvrši jednom
# radi ispravno, ali nema smisla koristiti for za samo jednu iteraciju
# for petlja korisna je kad želimo ponoviti nešto više puta, a ovdje se izvršava samo jednom — što bi se moglo jednostavno napisati i bez petlje:

print(1)